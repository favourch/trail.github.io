<p>
Snakobot involves making a robot which can traverse a snake-like path automatically with the help of pre-installed codes.
</p><br>
<h4>Bot Specification</h4>
<ol><li>MAXIMUM DIMENSION LIMIT OF BOT IS LENGTH×WIDTH×HEIGHT=30CM×30CM×25CM.</li><br><li>
THE MOTORS TO BE USED IS WITHIN THE RANGE OF 0-300 RPM. IT WILL BE CHECKED IN DIFFERENT CONDITIONS.</li><br>
<h4>POWER SUPPLY</h4>
<ol><li>SUPPLY OF 220VOLT, 50Hz AC WILL BE PROVIDED. </li><br><li>
TEAMS WILL HAVE TO BRING DOWN THE VOLTAGE LESS THAN 12 VOLTS.</li><br><li>
THE VOLTAGE AT ANY POINT ON THE ELECTRICAL CONNECTIONS SHOULD NOT EXCEED 12V.</li><br><li>
RECHARGABLE BATTERIES ARE ALSO ALLOWED.<br>
THE WIRELESS BOT SHOULD HAVE ITS OWN POWER SUPPLY.</li><br>
</ol>

