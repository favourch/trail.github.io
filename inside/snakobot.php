<div class="tab-pane fade" id="tab3">
<p>
<h3>ROUND 2</h3>
<ol><li>A maze will be provided follow the maze and reach to end point.</li><br>
<li>The actual arena would comprise of 4 checkpoints and the team would be awarded with 30 Marks for crossing each checkpoint(however it is not mandatory to cross all the checkpoints.)</li><br>
<li>For stopping at stop point additional 20 Marks will be awarded.</li><br>
<li>For indicating at stop point 20 Marks will be awarded.</li><br>
<li>A total of 5 minutes would be provided to each team to complete both the rounds and if failed the bot would be disqualified.</li><br>
<li>For each hand touch 10 20 30 marks will be consecutively deducted how-ever Maximum 4 hand touch are allowed in the 2nd round.</li><br>
<li>However if the bot completes the task on time, bonus points (300 - sec-onds used) would be awarded.</li><br></ol>     
</p>
</div>
<div class="tab-pane fade" id="tab4">
<p><H3>RULES</H3>
<ol><li>
The timing of the team would be started once the instructor gives the signal to start the bot.</li><br>
<li>Only a single team member would be allowed near the bot during the run.</li><br>
<li>All the bots must be submitted before starting of the actual competition.</li><br>
<li>However initially time would be provided to all the teams for the caliberation of the sensors and the rest components before commencement of the actual competition.</li><br>
<li>A total of three restarts would be provided to each team.</li><br>
<li>The restart have to be taken from the start line again.  </li><br>
<li>The bot reaching the end point first will qualify that round.</li><br>
<li>The bot reaching the end point first in the final round will be the winner.</li><br>
<li>The arena will be disclosed 10 minutes before the event starts. </li>
</ol>
</p>
</div>
<div class="tab-pane fade" id="tab5">
<p><H3>JUDGING CRITERIA</H3>
<ol><li>The teams would be awarded 30 points for crossing each checkpoint.</li><br>
<li>An additional 50 points would be provided to complete the DETECT RUN completely.</li><br>
<li>The teams wont be given any bonus time after 5 minutes.</li><br>
<li>At the end the team securing the highest points would be the winner.</li><br>
<li>No points would be deducted of the teams taking the restart.</li><br>
<li>if game is tied in 2nd round between any participants result of 1st round and accuracy of bot will be taken in consideration.</li>
</ol>
</p>
<p></p>
