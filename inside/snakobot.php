<p><h3>TEAM MEMBERS</h3>
<ol><li>A team would comprise a maximum of 4 members.</li><br>
<li>The team members should submit their name before commencement of the competition ALONG WITH A TEAM NAME.</li>
</ol>
</p>
</div>
<div class="tab-pane fade" id="tab7">
<p><H3>DISQUALIFICATION</H3>
<ol><li>No team in any condition would be given additional time.</li><br>
<li>Total of 5 minutes to each team would be provided, otherwise the team would be disqualified.</li><br>
<li>Once started the bot should not be touched manually (inspite of the con-dition to take a restart), otherwise the team would be disqualified.</li><br>
<li>The bot should not damage the arena in any way.
</li></ol>
