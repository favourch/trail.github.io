<h3>ROUND 1</h3>
<p>
<ol><li>
 IT is a qualifying round.</li>
<li>The participant who will follow the path from starting to ending point will be qualified for next round.</li><br>
<li>Maximum 3 hand touch are allowed.</li>
<li>This round will consist simple turns or simple 90 degrees turn.</li><br>
<li>However if the bot completes the task on time, bonus Marks (300 - sec-onds used) would be awarded.</li> <br>
<li>For stopping at stop point additional 20 Marks will be awarded.</li><br>
<li>For indicating at stop point 20 Marks will be awarded.</li><br>
<li>For each hand touch 10 20 30 marks will be consecutively deducted.</li><br>
</ol>


</p>
