<div class="tab-pane fade" id="tab7">
<p><H3>RULES</H3>
<ol><li>

The competition will be played on a point's basis.</li>

<li>The organizers reserve the right to change any of the rules as they deem fit.</li>

<li>Violation of any of the rules will result in immediate disqualification.</li>

<li>The maximum time limit for the clash of combatants inside the arena is 10 minutes.</li>

<li>A bot will be declared immobilize (out) if it cannot exhibit linear motion of at least one
inch within 30 seconds. A bot with one side of its drive train disabled will not be counted
out if it can demonstrate some degree of controlled movement.
</li>
<li> Teams that are not ready when called for battle will be considered to have declared a
walkover, and will receive no points.</li>



<li>The bot cannot be split into two subunits. Two distinct parts connected by a flexible
cable will be considered separate units.</li>
<li>Damaging the arena will lead to immediate disqualification.</li></ol>

In all cases, the judge's decision will be final and binding.


</p>
