<h4>
Rules for winning</h4>
 <ol><li>  For round 1 and round 2 winner will be decided on the point basis.</li><br>
<li> The teams qualifying the first two round will be allowed to fight in the third round which will be of knockout basis.  </li><br>
<li> A bot wins if its opponent is immobilized or thrown out of the arena, In case both the robots remain immobilize at the end of the round, the winner will be decided under the rules as discussed in the scoring pattern.  </li><br>
<li>Robots cannot win by pinning or lifting their opponents.  It will be allowed for a maximum of 15 seconds.</li></ol><br>
    

<h4>Scoring</h4>
<ol><li>
 Pushing a bot into a danger zone (red) will fetch 150 points.  </li><br>
<li> Cut mark (clearly visible) will fetch 50 points.  </li> <br>
<li> Any body part completely removed from a bot will fetch 250 points.  </li> <br> 4.  Completely lifting of opponents bot will fetch 100 points.  Robots cannot win by pinning or lifting their opponents.  It will be allowed for a maximum of 15 seconds.</li>
  </ol>

</p>
