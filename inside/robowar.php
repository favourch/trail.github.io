<h4>Size & Weight</h4>
<ol>
<li>The machine should fit in a box of dimension 750mm X 750mm X 1000 mm (1 X b X
h) at starting point of the match. <br>The external device used to control the machine or
any external tank is not included in the size constraint.</li>

<li> Weight of the robot should not exceed 60Kg +50/o tolerance (pneumatics/ tanks
included).
If the tank is external, its weight would be considered.
<br>
Weight of external power source (batteries and adaptors) will not be counted. Weight
of wireless wheeled robots will be counted.</li></ol>



<h4>Weapons System</h4>
<ul><li>
Robots can have any kind of cutters, spinner, flippers, saws, lifting devices, hammers etc.</li>
<li>As weapons with following exceptions and limitations:
<ul>
<li>Flame based weapons</li>
<li> Liquid projectiles</li>
<li> Any kind of inflammable liquid</li>
<li>Smoke/ Dust based weapons</li></ul></li></ul>

<h4>Mobility</h4>
Methods of mobility includes
<ol><li>Rolling (wheels, tracks or the whole robot).</li>
<li>Walking (linear actuated legs with no rolling or cam operated motion).</li>

<li>Jumping and hopping is not allowed.</li>
<li>

Flying is not allowed.</li></ol>
<h4>
Robot Controller Requirements</h4>
<ol><li>
 Bots may be wired or wireless. Autonomous bots not allowed.</li>
<li> It is recommended to use 3 to 4 frequencies for your robot.</li>

<li>For wired bots, care should be taken so that wires do not get entangled. Main
supply wires should be properly coated. The power supply should be 24
volts(max.).</li></ol>

</p>
