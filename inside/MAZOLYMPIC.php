<div class="tab-pane fade" id="tab4">
<h3>General Rules</h3><ol>
1. Only one team will be allowed to enter the arena at a time.<br>
2. Only One autonomous bot per team is allowed.<br>
3. A Sample arena will be provided for testing the robot on the day of the event, on which the
code and calibration can be done.<br>
4. After an allotted time on the sample arena, the robot will have to be submitted to the
organizers, following which the final rounds will be held.<br>
5. The final arena will be disclosed according to the below timeline, after which no submissions will
be allowed.<br>
6. In case of any conflict or confusion, the judgesâ€™ decision will be final. Judges reserve the right
to disqualify the teams using any unfair means during the competition.<br>
7. A team will comprise of maximum 4 members which may or may not be of the same institute.</ol>
</div>
<div class="tab-pane fade" id="tab5">
<h3>Game Rules</h3><ol>
1. The robot has to start from the starting zone. The robot must operate by
following the line.</br>
2. In round 2, some alternate paths may be made in between the path from start
to end.</br>
3. The provided arena may be different from the given sample arena.</br>
4. There will be checkpoints along the arena, through which points will be
awarded to the robot.</br>
5. If Hand-touch is required then the robot has to start from the previous
checkpoint.</br>
6. Maximum 3 hand touches are allowed, after which the maximum score
obtained till the third touch will be considered.</br>
7. Time shall be stopped when run is halted for a hand-touch.</br>
8. Maximum time limit given is 3 minutes for the first round, 5 minutes for the
second. Scores according to time is described in score section.</br>
9. The width of the path would be around 3-4cm.</br>
10. At the start of the task, the bot will be placed at the starting point. Only 1 team
member is allowed to be near the game field while starting the bot.</ol>
</div>
<div class="tab-pane fade" id="tab6">
<h3>AUTOBOT SPECIFICATIONS:</h3>
<ol>
1. Maximum size of the bot should not exceed 20cm X 20cm X 20cm.<br>
2. There is no restriction on the microcontroller family. You can use
any microcontroller like (AVR, PIC, ARDUINO etc.).<br>
3. The controller can be of 8 or 16 bit only.<br>
4. The maximum power supply is +12v DC.<br>
5. Bot must have on board power supply, i.e. Only wireless power
supplies ( cells, Batteries, etc ) are allowed. In emergency power
source will be provided at the Arena.<br>
6. The teams can use any kind of sensor for line following.<br>
</ol><br>
<h3>JUDGEMENT CRITERIA:-</h3>
Points and time will be the judgement criteria, if in any case points
are equal then time will be considered.
