<p>
A situation arise on nation security ,our army’s aircraft has been crashed in a
small island near Lakshadweep .Our quick response military teams start a rescue
mission called “AMPHIBIAN”………. Many teams participate in this operation which
make an environment of competition as par the order of carnal . So they
designed a manually controlled bot that has capable to run in water as well as
land , to cover maximum distance in shortest time interval, challenging the
hurdles and be one of the best opponents.</p><br>
