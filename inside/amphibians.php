<p><H3>EVENT RULE</H3>
<ol><li>
A team may consist of maximum 4 members.
<li> Bots would be checked for their safety before the event.</li>
<li> Organizers have power to change some rule before the event
start.</li>
<li> Points are given to each successful tasks and decided by event
head.</li>
<li> Points get minus(-) when bot get collide with the wall of
arena(water portion) , obstacles on tracks , bot not able to jump
on water and comeback to track from water ,etc.</li>
<li> .Bonus points are given when the bot reach the ‘end point’ before
the time limit.<br>
.If bot fails to reach the end point in time limit then the bot get
disqualify.<br>
.Rules are only change by event head and accepted by all
participants.<br>
.Time is given after minusing(-) some points when bot get damage
or not working properly during event in arena for repairing.</li></ol>
