<h4>
GAME PLAY</h4>
<li>
• The bot should be manually controlled.</li><br><li>
• Bots prepared could be wired or wireless as per the participants choice</li><br><li>
• Bots have to move in an arena specially designed for this event.</li><br><li>
• Bot starts from ‘‘starting point’’ cover the ground track and jump into water</li><br><li>
……. From water comeback to ground track and cover distance to ‘‘end
point’’.</li><br><li>
• Bots have to prick balloons ( by using only niddle only) or pick up objects of
different shapes and sizes(as per the stage requirements) and collect points
to proceed to the next stage.</li><br><li>
• Many challenges and hurdles come during the event which decide the
points….</li>
